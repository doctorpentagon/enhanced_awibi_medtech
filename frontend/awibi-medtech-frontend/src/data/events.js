// Function to search events
export const searchEvents = (searchTerm) => {
  if (!searchTerm) return events;
  
  const term = searchTerm.toLowerCase();
  return events.filter(event =>
    event.title.toLowerCase().includes(term) ||
    event.description.toLowerCase().includes(term) ||
    event.location.toLowerCase().includes(term) ||
    event.venue.toLowerCase().includes(term) ||
    event.country.toLowerCase().includes(term) ||
    event.region.toLowerCase().includes(term) ||
    event.type.toLowerCase().includes(term) ||
    event.category.toLowerCase().includes(term) ||
    event.organizer.toLowerCase().includes(term) ||
    event.tags.some(tag => tag.toLowerCase().includes(term))
  );
};

// Function to get events by location
export const getEventsByLocation = (location) => {
  return events.filter(event => 
    event.location.toLowerCase() === location.toLowerCase() ||
    event.country.toLowerCase() === location.toLowerCase()
  );
};

// Function to get events by type
export const getEventsByType = (type) => {
  return events.filter(event => event.type === type);
};

// Function to get events by category
export const getEventsByCategory = (category) => {
  return events.filter(event => event.category === category);
};

// Function to get upcoming events
export const getUpcomingEvents = () => {
  const today = new Date();
  return events.filter(event => new Date(event.date) >= today);
};

// Function to get featured events
export const getFeaturedEvents = () => {
  return events.filter(event => event.featured);
};

// Function to get events by date range
export const getEventsByDateRange = (startDate, endDate) => {
  return events.filter(event => {
    const eventDate = new Date(event.date);
    return eventDate >= new Date(startDate) && eventDate <= new Date(endDate);
  });
};

// Function to get all event types
export const getAllEventTypes = () => {
  return [...new Set(events.map(event => event.type))].sort();
};

// Function to get all event categories
export const getAllEventCategories = () => {
  return [...new Set(events.map(event => event.category))].sort();
};

// Function to get all event locations
export const getAllEventLocations = () => {
  return [...new Set(events.map(event => event.location))].sort();
};

// Function to get all event countries
export const getAllEventCountries = () => {
  return [...new Set(events.map(event => event.country))].sort();
};

